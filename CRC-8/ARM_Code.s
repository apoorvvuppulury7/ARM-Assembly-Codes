	AREA CRC,CODE,READONLY
START
	LDR R0,=0x654321 ;INPUT DATA
	LDR R1,=0x18	 ;INPUT DATA LENGTH
	MOV R2, R0, LSL #8	 ;Shifted DataLDR  
	LDR R3,=0x107    ;POLYNOMIAL
	ADD R4, R1, #0x8 ;Repreents length of shifted data
	SUB R5, R4, #0x1 ;Counter
	LDR R7, =0x0
	SUB R8, R1, #0x1 
	MOV R6, R3, LSL R8
	
LOOP_SHIFT
		SUB R10, R6, R3
		CMP R10, #0x0
		BEQ LOOPCHECK
		MOV R9, R2, LSR R5
		AND R9, R9, #0x1
		CMP R9, #0x0
		BEQ LOOP_M0
		BNE LOOP_M1
			
LOOP_M0
		MOV R6, R6, LSR #0x1
		SUB R5, R5, #0x1
		B LOOP_SHIFT
	
LOOP_M1
		EOR R2, R2, R6
		MOV R6, R6, LSR #0x1
		SUB R5, R5, #0x1
		B LOOP_SHIFT
			
LOOPCHECK
		CMP R2, R6
		BLT LOOP
		EOR R2,R2,R6
		B LOOP
	
LOOP
		B LOOP
	END